-- SPCau1
CREATE OR ALTER PROCEDURE SPCau1
    @TenCLB NVARCHAR(100),
    @TenQG NVARCHAR(60)
AS
BEGIN
    SELECT ct.MACT, ct.HOTEN, ct.NGAYSINH, ct.DIACHI, ct.VITRI
    FROM CAUTHU ct
    JOIN CAULACBO clb ON ct.MACLB = clb.MACLB
    JOIN QUOCGIA qg ON ct.MAQG = qg.MAQG
    WHERE clb.TENCLB = @TenCLB
      AND qg.TENQG = @TenQG
END
GO

-- SPCau2
CREATE OR ALTER PROCEDURE SPCau2
    @Vong INT,
    @Nam INT
AS
BEGIN
    SELECT td.MATRAN, td.NGAYTD, s.TENSAN,
           clb1.TENCLB AS TENCLB1,
           clb2.TENCLB AS TENCLB2,
           td.KETQUA
    FROM TRANDAU td
    JOIN SANVD s ON td.MASAN = s.MASAN
    JOIN CAULACBO clb1 ON td.MACLB1 = clb1.MACLB
    JOIN CAULACBO clb2 ON td.MACLB2 = clb2.MACLB
    WHERE td.VONG = @Vong
      AND td.NAM = @Nam
END
GO

-- SPCau3
CREATE OR ALTER PROCEDURE SPCau3
    @TenQG NVARCHAR(60)
AS
BEGIN
    SELECT hlv.MAHLV, hlv.TENHLV, hlv.NGAYSINH,
           hlv.DIACHI, hclb.VAITRO, clb.TENCLB
    FROM HUANLUYENVIEN hlv
    JOIN HLV_CLB hclb ON hlv.MAHLV = hclb.MAHLV
    JOIN CAULACBO clb ON hclb.MACLB = clb.MACLB
    JOIN QUOCGIA qg ON hlv.MAQG = qg.MAQG
    WHERE qg.TENQG = @TenQG
END
GO

-- SPCau4
CREATE OR ALTER PROCEDURE SPCau4
    @TenQG NVARCHAR(60)
AS
BEGIN
    SELECT clb.MACLB, clb.TENCLB, s.TENSAN, s.DIACHI,
           COUNT(*) AS SoCauThuNuocNgoai
    FROM CAULACBO clb
    JOIN SANVD s ON clb.MASAN = s.MASAN
    JOIN CAUTHU ct ON clb.MACLB = ct.MACLB
    JOIN QUOCGIA qg ON ct.MAQG = qg.MAQG
    WHERE qg.TENQG <> @TenQG
    GROUP BY clb.MACLB, clb.TENCLB, s.TENSAN, s.DIACHI
    HAVING COUNT(*) > 2
END
GO

