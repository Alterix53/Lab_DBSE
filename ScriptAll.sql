-- SPCau9
CREATE OR ALTER PROCEDURE SPCau9
    @Vong INT,
    @Nam INT
AS
BEGIN
    DECLARE @MACLB VARCHAR(5)

    SELECT @MACLB = MACLB
    FROM BANGXH
    WHERE VONG = @Vong
      AND NAM = @Nam
      AND HANG = 1

    SELECT td.NGAYTD, s.TENSAN,
           clb1.TENCLB AS TENCLB1,
           clb2.TENCLB AS TENCLB2,
           td.KETQUA
    FROM TRANDAU td
    JOIN SANVD s ON td.MASAN = s.MASAN
    JOIN CAULACBO clb1 ON td.MACLB1 = clb1.MACLB
    JOIN CAULACBO clb2 ON td.MACLB2 = clb2.MACLB
    WHERE td.MACLB1 = @MACLB
       OR td.MACLB2 = @MACLB
END
GO

-- SPCau10
CREATE OR ALTER PROCEDURE SPCau10
    @Vong INT,
    @Nam INT
AS
BEGIN
    DECLARE @MACLB VARCHAR(5)

    SELECT @MACLB = MACLB
    FROM BANGXH
    WHERE VONG = @Vong
      AND NAM = @Nam
      AND HANG = (
            SELECT MAX(HANG)
            FROM BANGXH
            WHERE VONG = @Vong
              AND NAM = @Nam
      )

    SELECT td.NGAYTD, s.TENSAN,
           clb1.TENCLB AS TENCLB1,
           clb2.TENCLB AS TENCLB2,
           td.KETQUA
    FROM TRANDAU td
    JOIN SANVD s ON td.MASAN = s.MASAN
    JOIN CAULACBO clb1 ON td.MACLB1 = clb1.MACLB
    JOIN CAULACBO clb2 ON td.MACLB2 = clb2.MACLB
    WHERE td.MACLB1 = @MACLB
       OR td.MACLB2 = @MACLB
END
