-- store procedure 1
CREATE PROCEDURE SP_SEL_NO_ENCRYPT
    @TenCLB NVARCHAR(100),
    @TenQG  NVARCHAR(60)
AS
BEGIN
    SELECT 
        CT.MACT,
        CT.HOTEN,
        CT.NGAYSINH,
        CT.DIACHI,
        CT.VITRI
    FROM CAUTHU CT
    JOIN CAULACBO CLB ON CT.MACLB = CLB.MACLB
    JOIN QUOCGIA QG ON CT.MAQG = QG.MAQG
    WHERE CLB.TENCLB = @TenCLB
      AND QG.TENQG = @TenQG
END;
-- store procedure 2
GO
CREATE PROCEDURE SP_SEL_ENCRYPT
    @TenCLB NVARCHAR(100),
    @TenQG  NVARCHAR(60)
WITH ENCRYPTION -- MA HOA
AS
BEGIN
    SELECT 
        CT.MACT,
        CT.HOTEN,
        CT.NGAYSINH,
        CT.DIACHI,
        CT.VITRI
    FROM CAUTHU CT
    JOIN CAULACBO CLB ON CT.MACLB = CLB.MACLB
    JOIN QUOCGIA QG ON CT.MAQG = QG.MAQG
    WHERE CLB.TENCLB = @TenCLB
      AND QG.TENQG = @TenQG
END;
GO


-- cau i
--  vCau1 1) Cầu thủ thuộc “SHB Đà Nẵng” có quốc tịch “Brazil”
CREATE OR ALTER VIEW dbo.vCau1
AS
SELECT
    CT.MACT,
    CT.HOTEN,
    CT.NGAYSINH,
    CT.DIACHI,
    CT.VITRI
FROM dbo.CAUTHU AS CT
JOIN dbo.CAULACBO AS CLB ON CT.MACLB = CLB.MACLB
JOIN dbo.QUOCGIA  AS QG  ON CT.MAQG  = QG.MAQG
WHERE CLB.TENCLB = N'SHB ĐÀ NẴNG'
  AND QG.TENQG  = N'Brazil';
GO

--  vCau2 2) Kết quả các trận vòng 3, năm 2009
CREATE OR ALTER VIEW dbo.vCau2
AS
SELECT
    TD.MATRAN,
    TD.NGAYTD,
    S.TENSAN,
    CLB1.TENCLB AS TENCLB1,
    CLB2.TENCLB AS TENCLB2,
    TD.KETQUA
FROM dbo.TRANDAU AS TD
JOIN dbo.SANVD   AS S    ON TD.MASAN  = S.MASAN
JOIN dbo.CAULACBO AS CLB1 ON TD.MACLB1 = CLB1.MACLB
JOIN dbo.CAULACBO AS CLB2 ON TD.MACLB2 = CLB2.MACLB
WHERE TD.NAM = 2009
  AND TD.VONG = 3;
GO

--  vCau3 3) HLV quốc tịch “Việt Nam” + CLB đang làm việc
CREATE OR ALTER VIEW dbo.vCau3
AS
SELECT
    HLV.MAHLV,
    HLV.TENHLV,
    HLV.NGAYSINH,
    HLV.DIACHI,
    HC.VAITRO,
    CLB.TENCLB
FROM dbo.HUANLUYENVIEN AS HLV
JOIN dbo.QUOCGIA       AS QG  ON HLV.MAQG = QG.MAQG
JOIN dbo.HLV_CLB       AS HC  ON HLV.MAHLV = HC.MAHLV
JOIN dbo.CAULACBO      AS CLB ON HC.MACLB  = CLB.MACLB
WHERE QG.TENQG = N'Việt Nam';
GO

--vCau4 4) CLB + sân + địa chỉ + số cầu thủ nước ngoài (>2) (nước ngoài = quốc tịch khác “Việt Nam”)
CREATE OR ALTER VIEW dbo.vCau4
AS
SELECT
    CLB.MACLB,
    CLB.TENCLB,
    S.TENSAN,
    S.DIACHI,
    COUNT(*) AS SoLuongCauThuNuocNgoai
FROM dbo.CAULACBO AS CLB
JOIN dbo.SANVD    AS S   ON CLB.MASAN = S.MASAN
JOIN dbo.CAUTHU   AS CT  ON CT.MACLB  = CLB.MACLB
JOIN dbo.QUOCGIA  AS QG  ON CT.MAQG   = QG.MAQG
WHERE QG.TENQG <> N'Việt Nam'
GROUP BY
    CLB.MACLB, CLB.TENCLB, S.TENSAN, S.DIACHI
HAVING COUNT(*) > 2;
GO

-- vCau5 5) Tên tỉnh + số lượng cầu thủ vị trí “Tiền đạo” trong các CLB thuộc tỉnh đó
CREATE OR ALTER VIEW dbo.vCau5
AS
SELECT
    T.TENTINH,
    COUNT(*) AS SoLuongTienDao
FROM dbo.TINH      AS T
JOIN dbo.CAULACBO  AS CLB ON CLB.MATINH = T.MATINH
JOIN dbo.CAUTHU    AS CT  ON CT.MACLB   = CLB.MACLB
WHERE CT.VITRI = N'Tiền đạo'
GROUP BY T.TENTINH;
GO

-- vCau6 6) Tên CLB + tên tỉnh của CLB đứng hạng cao nhất ở BXH vòng 3 năm 2009 
CREATE OR ALTER VIEW dbo.vCau6
AS
SELECT TOP (1)
    CLB.TENCLB,
    T.TENTINH
FROM dbo.BANGXH   AS BXH
JOIN dbo.CAULACBO AS CLB ON BXH.MACLB  = CLB.MACLB
JOIN dbo.TINH     AS T   ON CLB.MATINH = T.MATINH
WHERE BXH.NAM = 2009
  AND BXH.VONG = 3
ORDER BY BXH.HANG ASC;
GO

-- vCau7 7) Tên HLV đang giữ vị trí trong CLB nhưng chưa có SĐT
CREATE OR ALTER VIEW dbo.vCau7
AS
SELECT DISTINCT
    HLV.TENHLV
FROM dbo.HUANLUYENVIEN AS HLV
JOIN dbo.HLV_CLB       AS HC ON HLV.MAHLV = HC.MAHLV
WHERE HLV.DIENTHOAI IS NULL
   OR LTRIM(RTRIM(HLV.DIENTHOAI)) = N'';
GO

-- vCau8 8) HLV quốc tịch Việt Nam chưa làm HLV ở CLB nào
CREATE OR ALTER VIEW dbo.vCau8
AS
SELECT
    HLV.MAHLV,
    HLV.TENHLV,
    HLV.NGAYSINH,
    HLV.DIACHI,
    HLV.DIENTHOAI
FROM dbo.HUANLUYENVIEN AS HLV
JOIN dbo.QUOCGIA       AS QG ON HLV.MAQG = QG.MAQG
LEFT JOIN dbo.HLV_CLB  AS HC ON HLV.MAHLV = HC.MAHLV
WHERE QG.TENQG = N'Việt Nam'
  AND HC.MAHLV IS NULL;
GO

--  vCau9 9) DS trận đấu của CLB xếp hạng cao nhất tính đến hết vòng 3 năm 2009
CREATE OR ALTER VIEW dbo.vCau9
AS
WITH TopCLB AS
(
    SELECT TOP (1) BXH.MACLB
    FROM dbo.BANGXH AS BXH
    WHERE BXH.NAM = 2009 AND BXH.VONG = 3
    ORDER BY BXH.HANG ASC
)
SELECT
    TD.NGAYTD,
    S.TENSAN,
    CLB1.TENCLB AS TENCLB1,
    CLB2.TENCLB AS TENCLB2,
    TD.KETQUA
FROM dbo.TRANDAU AS TD
JOIN dbo.SANVD   AS S    ON TD.MASAN  = S.MASAN
JOIN dbo.CAULACBO AS CLB1 ON TD.MACLB1 = CLB1.MACLB
JOIN dbo.CAULACBO AS CLB2 ON TD.MACLB2 = CLB2.MACLB
CROSS JOIN TopCLB AS T
WHERE TD.NAM = 2009
  AND TD.VONG <= 3
  AND (TD.MACLB1 = T.MACLB OR TD.MACLB2 = T.MACLB);
GO

--  vCau10 10) DS trận đấu của CLB hạng thấp nhất trong BXH vòng 3 năm 2009
CREATE OR ALTER VIEW dbo.vCau10
AS
WITH BottomCLB AS
(
    SELECT TOP (1) BXH.MACLB
    FROM dbo.BANGXH AS BXH
    WHERE BXH.NAM = 2009 AND BXH.VONG = 3
    ORDER BY BXH.HANG DESC
)
SELECT
    TD.NGAYTD,
    S.TENSAN,
    CLB1.TENCLB AS TENCLB1,
    CLB2.TENCLB AS TENCLB2,
    TD.KETQUA
FROM dbo.TRANDAU AS TD
JOIN dbo.SANVD   AS S    ON TD.MASAN  = S.MASAN
JOIN dbo.CAULACBO AS CLB1 ON TD.MACLB1 = CLB1.MACLB
JOIN dbo.CAULACBO AS CLB2 ON TD.MACLB2 = CLB2.MACLB
CROSS JOIN BottomCLB AS B
WHERE TD.NAM = 2009
  AND TD.VONG <= 3
  AND (TD.MACLB1 = B.MACLB OR TD.MACLB2 = B.MACLB);
GO

